# Tipos Básicos en Python al crear la variable se asigna el tipo de dato sin necesidad de indicarselo a diferencia de
# otros lenguajes como Java, Kotlin, TypeScript, etc.

cadena = "Hola mundo"

enteros = 21

condicion = True

realFloat = 3.141592653589793
