# Diferentes operadores que podemos utilizar y disponemos en Python

# Operadores aritmeticos
suma = 1 + 2
resta = 1 - 2
multiplicacion = 1 * 2
division = 1 / 2
modulo = 1 % 2
exponencial = 2 ** 2
divisionEntera = 4 // 2

print(suma)
print(resta)
print(multiplicacion)
print(division)
print(modulo)
print(exponencial)
print(divisionEntera)

# Operadores a nivel de bit
operadorY = suma & resta
operadorO = suma | resta
OperadorAnd = operadorY and operadorO
OperadorOr = operadorY or operadorO

print(operadorY)
print(operadorO)
print(OperadorAnd)
print(OperadorOr)

# Operadores relacionales
operadorIgual = suma == resta
operadorDiferente = suma!= resta
operadorMayor = suma > resta
operadorMenor = suma < resta
operadorMayorIgual = suma >= resta
operadorMenorIgual = suma <= resta

print(operadorIgual)
print(operadorDiferente)
print(operadorMayor)
print(operadorMenor)
print(operadorMayorIgual)
print(operadorMenorIgual)

